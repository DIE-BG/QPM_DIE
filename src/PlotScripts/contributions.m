            SimTools.scripts.pausaGuarda(...
            fullfile(params.SavePath, 'long',...
            sprintf("%s_%s.png", 'MCI', params.date_ant)), ...
            'AutoSave', true);

    elseif rng{1} == params.StartDate{2}
            SimTools.scripts.pausaGuarda(...
            fullfile(params.SavePath, 'short',...
            sprintf("%s_%s_short.png", 'MCI', params.date_ant)), ...
