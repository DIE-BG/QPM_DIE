    bar(rng{1}:params.EndDatePlot{1},...
        [full_data_add.F_pred.b4*full_data_add.F_pred.RR_GAP (1-full_data_add.F_pred.b4)*-1*full_data_add.F_pred.L_Z_GAP,...
        -full_data_add.F_pred.b6*(full_data_add.F_pred.D4L_MB.shift(-1) - full_data_add.F_pred.ss_D4L_MB)],...
        'stacked');
    
