          full_data_add.DATES.hist_end,...
          'LineWidth', 1, ...
          'LineStyle', '-.');
      
    else
    vline(...
          MODEL.DATES.hist_end,...
          'LineWidth', 1, ...
          'LineStyle', '-.');    
    
    end
        
    hold off
    
    legend({'Tasa Real (brecha)', 'Tipo de Cambio Real (brecha)',...
        'Base Monetaria (rezago)'},...
        'FontSize', 14,...
        'Location', 'Best', 'Interpreter', 'none')
    
    colororder([0.345 0.922 0.988;...
                0.729 0.98 0.306;...
                0.804 0.345 0.988;])

    if strcmp(params.Esc_add{1}, 'v0')
