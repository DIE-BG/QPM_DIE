% MODEL.Esc.v6.dbi.D4S4L_GDP(qq(2025,4)) = 3.5; 
% anchor_dates = MODEL.DATES.E6_dates;

% Variable a exogenizar (Anclaje)
MODEL.Esc.v6.planSim = exogenize(MODEL.Esc.v6.planSim,{'D4S4L_GDP'}, anchor_dates);

% Variable a endogenizar (shock propio?? No necesariamente)
endog_vars = {'SHK_L_GDP_GAP'}; 
MODEL.Esc.v6.planSim = endogenize(MODEL.Esc.v6.planSim, endog_vars, anchor_dates); 


% Simulación.
MODEL.Esc.v6.pred = simulate(MODEL.MF,...
                  MODEL.Esc.v6.dbi,...
                  MODEL.DATES.pred_start:MODEL.DATES.pred_end,...
                  'plan',MODEL.Esc.v6.planSim,...
                  'anticipate', false, ...
                  'DbOverlay=', true);

% Descomposición             
MODEL.Esc.v6.shd = simulate(MODEL.MF,...
                  MODEL.Esc.v6.pred,...
                  MODEL.DATES.hist_start:MODEL.DATES.pred_end,...
                  'anticipate', false, ...
                  'contributions',true);

