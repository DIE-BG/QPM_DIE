%% Anclaje crecimiento económico diciembres (8 trimestres)

% Trimestres de anclaje
MODEL.DATES.E6_dates = MODEL.DATES.pred_start:MODEL.DATES.pred_start+5;

%% %%%%%%%%%%%%%%%% Creación de escenario alternativo %%%%%%%%%%%%%%%%%%%%%%
% Shocks del escenario base
shocks = MODEL.F*get(MODEL.MF, 'elist');
% Concatenación de bd con condiciones iniciales (MODEL.F) y shocks Esc.
% Libre
MODEL.Esc.v6.dbi = dboverlay(MODEL.F,shocks);

% Plan de simulación
MODEL.Esc.v6.planSim = plan(MODEL.MF, MODEL.DATES.pred_start:MODEL.DATES.pred_end);


% Imposición de anclajes de crecimiento

% Anclaje únicamente fines de año
MODEL.Esc.v6.dbi.D4S4L_GDP(MODEL.DATES.E6_dates) = [ ...
    NaN; 3.5; NaN; NaN; NaN; 3.5; ... 
];
anchor_dates = [qq(2024,4), qq(2025,4)];

% Anclaje trayectoria hasta 2025Q4
% MODEL.Esc.v6.dbi.D4S4L_GDP(MODEL.DATES.E6_dates) = MODEL.F_pred.D4S4L_GDP(MODEL.DATES.E6_dates); 
